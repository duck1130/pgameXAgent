# for 迴圈搭配 range 範例

# 範例1：只給結束值，預設從 0 開始，每次加 1
for i in range(5):
    print(i)  # 輸出 0 到 4 的數字

# 迴圈變數 i 每回合會從範圍內取一個值，直到範圍結束
# 這裡的 i 會依次取 0, 1, 2, 3, 4

# range() 可以設定起始值、結束值和步長（不包含結束值）
# 語法：range(起始值, 結束值, 步長)

# 範例2：指定起始值與結束值
for i in range(2, 6):
    print(i)  # 輸出 2, 3, 4, 5

# 範例3：指定起始值、結束值與步長
for i in range(1, 10, 2):
    print(i)  # 輸出 1, 3, 5, 7, 9

# 範例4：反向遞減
for i in range(10, 0, -2):
    print(i)  # 輸出 10, 8, 6, 4, 2

# 也可以只產生 range 物件（不執行迴圈）
r = range(1, 10, 2)  # 從 1 到 9，每次增加 2
print(list(r))  # 輸出 [1, 3, 5, 7, 9]
